package lr4;

public class example1 {
    public static void main(String[] args) {
        int iWidth = 23;
        int iHeight = 11;

        for (int i = 0; i < iWidth; i++) {
            for (int j = 0; j < iHeight; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
